  // Method untuk mendapatkan semua consonants berdasarkan aksen
  static List<Map<String, String>> getConsonants(String accent) {
    return accent.toLowerCase() == 'british' ? britishConsonants : americanConsonants;
  }

  // Method untuk mencari consonant tertentu dalam kedua aksen
  static Map<String, String>? findConsonant(String letter, {String? accent}) {
    if (accent != null) {
      final consonants = getConsonants(accent);
      return consonants.firstWhere(
            (consonant) => consonant['letter'] == letter,
        orElse: () => {},
      );
    }

    // Cari di American terlebih dahulu
    final americanConsonant = americanConsonants.firstWhere(
          (consonant) => consonant['letter'] == letter,
      orElse: () => {},
    );

    if (americanConsonant.isNotEmpty) return americanConsonant;

    // Jika tidak ditemukan di American, cari di British
    return britishConsonants.firstWhere(
          (consonant) => consonant['letter'] == letter,
      orElse: () => {},
    );
  }
