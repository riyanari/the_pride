  // Method untuk mendapatkan semua vowels berdasarkan aksen
  static List<Map<String, String>> getVowels(String accent) {
    return accent.toLowerCase() == 'british' ? britishVowels : americanVowels;
  }

  // Method untuk mencari vowel tertentu dalam kedua aksen
  static Map<String, String>? findVowel(String letter, {String? accent}) {
    if (accent != null) {
      final vowels = getVowels(accent);
      return vowels.firstWhere(
            (vowel) => vowel['letter'] == letter,
        orElse: () => {},
      );
    }

    // Cari di American terlebih dahulu
    final americanVowel = americanVowels.firstWhere(
          (vowel) => vowel['letter'] == letter,
      orElse: () => {},
    );

    if (americanVowel.isNotEmpty) return americanVowel;

    // Jika tidak ditemukan di American, cari di British
    return britishVowels.firstWhere(
          (vowel) => vowel['letter'] == letter,
      orElse: () => {},
    );
  }
